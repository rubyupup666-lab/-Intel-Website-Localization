function setLanguage(lang) {
  const selected = translations[lang] ? lang : "en";
  const dictionary = translations[selected];

  document.documentElement.lang = selected;
  document.documentElement.dir = selected === "ar" ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    if (dictionary[key]) {
      element.placeholder = dictionary[key];
    }
  });

  document.querySelectorAll("img[data-alt-en]").forEach((image) => {
    image.alt = selected === "ar" ? image.dataset.altAr : image.dataset.altEn;
  });

  const languageSelect = document.getElementById("languageSelect");
  languageSelect.value = selected;
  localStorage.setItem("preferredLanguage", selected);
}

const savedLanguage = localStorage.getItem("preferredLanguage");
const detectedLanguage = navigator.language && navigator.language.toLowerCase().startsWith("ar") ? "ar" : "en";
setLanguage(savedLanguage || detectedLanguage);

document.getElementById("languageSelect").addEventListener("change", (event) => {
  setLanguage(event.target.value);
});

document.getElementById("subscribeForm").addEventListener("submit", (event) => {
  event.preventDefault();
  const currentLanguage = document.documentElement.lang || "en";
  document.getElementById("form-status").textContent = translations[currentLanguage].successMessage;
  event.target.reset();
});
